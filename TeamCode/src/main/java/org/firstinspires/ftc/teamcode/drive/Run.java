package org.firstinspires.ftc.teamcode.drive;

import com.acmerobotics.dashboard.config.Config;
import com.acmerobotics.roadrunner.geometry.Pose2d;
import com.qualcomm.robotcore.eventloop.opmode.LinearOpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;
import com.qualcomm.robotcore.hardware.DcMotor;
import com.qualcomm.robotcore.hardware.DcMotorSimple;
import com.qualcomm.robotcore.util.ElapsedTime;

import org.firstinspires.ftc.robotcore.external.hardware.camera.WebcamName;
import org.firstinspires.ftc.teamcode.drive.SampleMecanumDrive;
import org.firstinspires.ftc.teamcode.util.RobotUtils;
import org.openftc.apriltag.AprilTagDetection;
import org.openftc.easyopencv.OpenCvCamera;
import org.openftc.easyopencv.OpenCvCameraFactory;
import org.openftc.easyopencv.OpenCvCameraRotation;

import java.util.ArrayList;

@TeleOp(name="Run",group = "teleop")
@Config
public class Run extends LinearOpMode {
    private RobotUtils robot;
    OpenCvCamera webcam;

    org.firstinspires.ftc.teamcode.util.detection.DetectionPipeline detectionPipeline;

    boolean bCameraOpened = false;
    private SampleMecanumDrive drive;


    static final double FEET_PER_METER = 3.28084;

    // Lens intrinsics
    // UNITS ARE PIXELS
    // NOTE: this calibration is for the C920 webcam at 800x448.
    // You will need to do your own calibration for other configurations!
    double fx = 578.272;
    double fy = 578.272;
    double cx = 402.145;
    double cy = 221.506;

    // UNITS ARE METERS
    double tagsize = 0.166;

    // Tag ID 1,2,3 from the 36h11 family
    /*EDIT IF NEEDED!!!*/

    int LEFT = 1;
    int MIDDLE = 2;
    int RIGHT = 3;
    org.firstinspires.ftc.teamcode.util.detection.AprilTagDetectionPipeline aprilTagDetectionPipeline;
    AprilTagDetection tagOfInterest = null;

    public void runOpMode() throws InterruptedException {

        int cameraMonitorViewId = hardwareMap.appContext.getResources().getIdentifier("cameraMonitorViewId", "id", hardwareMap.appContext.getPackageName());
        webcam = OpenCvCameraFactory.getInstance().createWebcam(hardwareMap.get(WebcamName.class, "Webcam 1"), cameraMonitorViewId);

        aprilTagDetectionPipeline = new org.firstinspires.ftc.teamcode.util.detection.AprilTagDetectionPipeline(tagsize, fx, fy, cx, cy);


        detectionPipeline = new org.firstinspires.ftc.teamcode.util.detection.DetectionPipeline();
        webcam.setPipeline(aprilTagDetectionPipeline);

        webcam.openCameraDeviceAsync(new OpenCvCamera.AsyncCameraOpenListener() {
            @Override
            public void onOpened() {
                bCameraOpened = true;
                webcam.startStreaming(640, 480, OpenCvCameraRotation.UPRIGHT);
            }

            @Override
            public void onError(int errorCode) {

            }
        });


        SampleMecanumDrive drive = new SampleMecanumDrive(hardwareMap);

        robot = new RobotUtils(hardwareMap);

        robot.extend();
        robot.open_intake();

        org.firstinspires.ftc.teamcode.util.detection.DetectionPipeline pipeline = new org.firstinspires.ftc.teamcode.util.detection.DetectionPipeline();

        waitForStart();

        if (isStopRequested()) return;

        while (opModeIsActive() && !isStopRequested()) {

            while (!isStarted() && !isStopRequested())
            {
                ArrayList<AprilTagDetection> currentDetections = aprilTagDetectionPipeline.getLatestDetections();

                if(currentDetections.size() != 0)
                {
                    boolean tagFound = false;

                    for(AprilTagDetection tag : currentDetections)
                    {
                        if(tag.id == LEFT || tag.id == MIDDLE || tag.id == RIGHT)
                        {
                            tagOfInterest = tag;
                            tagFound = true;
                            break;
                        }
                    }

                    if(tagFound)
                    {
                        telemetry.addLine("Tag of interest is in sight!\n\nLocation data:");
                        tagToTelemetry(tagOfInterest);
                    }
                    else
                    {
                        telemetry.addLine("Don't see tag of interest :(");

                        if(tagOfInterest == null)
                        {
                            telemetry.addLine("(The tag has never been seen)");
                        }
                        else
                        {
                            telemetry.addLine("\nBut we HAVE seen the tag before; last seen at:");
                            tagToTelemetry(tagOfInterest);
                        }
                    }

                }
                else
                {
                    telemetry.addLine("Don't see tag of interest :(");

                    if(tagOfInterest == null)
                    {
                        telemetry.addLine("(The tag has never been seen)");
                    }
                    else
                    {
                        telemetry.addLine("\nBut we HAVE seen the tag before; last seen at:");
                        tagToTelemetry(tagOfInterest);
                    }

                }

                telemetry.update();
                sleep(20);
            }

            if(tagOfInterest != null)
            {
                telemetry.addLine("Tag snapshot:\n");
                tagToTelemetry(tagOfInterest);
                telemetry.update();
            }
            else
            {
                telemetry.addLine("No tag snapshot available, it was never sighted during the init loop :(");
                telemetry.update();
            }

            if(tagOfInterest == null){
            robot.extend();
            robot.open_intake();
            }
            else{
            switch(tagOfInterest.id){
                case 1:
                    sleep(1000);
                    robot.close_intake();
                    sleep(100);
                    robot.retract();
                    sleep(500);
                    robot.goUp();
                    robot.goTurretLeft();
                    sleep(2000);
                    robot.extend();
                    sleep(1500);
                    robot.open_intake();
                    sleep(200);
                    robot.retract();
                    sleep(500);
                    robot.goTurretCenter();
                    robot.goDown();
                    sleep(1500);
                    robot.extend();
                    break;
                case 2:
                    sleep(1000);
                    robot.close_intake();
                    sleep(100);
                    robot.retract();
                    sleep(500);
                    robot.goUp();
                    robot.goTurretRight();
                    sleep(2000);
                    robot.extend();
                    sleep(1500);
                    robot.open_intake();
                    sleep(200);
                    robot.retract();
                    sleep(500);
                    robot.goTurretCenter();
                    robot.goDown();
                    sleep(1500);
                    robot.extend();
                    break;
                case 3:
                    sleep(1000);
                    robot.close_intake();
                    sleep(100);
                    robot.retract();
                    sleep(500);
                    robot.goUp();
                    robot.goTurretCenter();
                    sleep(2000);
                    robot.extend();
                    sleep(1500);
                    robot.open_intake();
                    sleep(200);
                    robot.retract();
                    sleep(500);
                    robot.goTurretCenter();
                    robot.goDown();
                    sleep(1500);
                    robot.extend();
                    break;
            }
        }

            drive.update();

        }
        telemetry.update();
    }
}

